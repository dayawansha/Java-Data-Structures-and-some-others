package wrapperclsb;

public class WrapperClsB {

   
    public static void main(String[] args) {
        
       Float a = (float) 2.3;    // double constrocter
       Float b = 2.4f;   // float constructor
       Float c = new Float("2.5");  // string constructor
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
    
}
